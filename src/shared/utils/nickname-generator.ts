/**
 * 유저 친화적인 닉네임을 생성하는 함수
 * "따사로운라이언", "친절한고스트" 같은 형태로 생성
 */

const adjectives = [
  '따사로운', '친절한', '상냥한', '활발한', '밝은', '따뜻한', '귀여운', '재미있는',
  '용감한', '지혜로운', '행복한', '즐거운', '신나는', '멋진', '예쁜', '착한',
  '똑똑한', '유쾌한', '부드러운', '다정한', '정겨운', '포근한', '상쾌한', '시원한',
  '아름다운', '고운', '훌륭한', '든든한', '튼튼한', '건강한', '활기찬', '생생한'
];

const nouns = [
  '라이언', '고스트', '토끼', '곰돌이', '고양이', '강아지', '팬더', '코알라',
  '호랑이', '사자', '코끼리', '기린', '펭귄', '돌고래', '부엉이', '다람쥐',
  '햄스터', '여우', '늑대', '사슴', '말', '양', '소', '돼지', '닭', '오리',
  '독수리', '매', '비둘기', '까마귀', '참새', '제비', '학', '거북이', '물고기'
];

/**
 * 랜덤한 유저 친화적 닉네임을 생성합니다
 * @returns 형용사 + 명사 형태의 닉네임 (예: "따사로운라이언")
 */
export function generateFriendlyNickname(): string {
  const randomAdjective = adjectives[Math.floor(Math.random() * adjectives.length)];
  const randomNoun = nouns[Math.floor(Math.random() * nouns.length)];
  
  return `${randomAdjective}${randomNoun}`;
}